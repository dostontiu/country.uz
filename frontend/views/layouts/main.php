    <?php $this->head() ?>
</head>
<body>
<?php $this->beginBody() ?>

<!--<div class="wrap">-->
    <?php
    NavBar::begin([
        'brandLabel' => 'Home',
        'brandUrl' => Yii::$app->homeUrl,
        'options' => [
            'class' => 'navbar navbar-default navbar-fixed-top',
            'id' => 'siteNav',
        ],
    ]);
    $menuItems = [
        ['label' => 'Login', 'url' => ['/admin']],
    ];
